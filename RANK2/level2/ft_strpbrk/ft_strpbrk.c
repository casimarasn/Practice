#include <stdio.h>

char	*ft_strpbrk(const char *s1, const char *s2)
{
	int	i;
	int	j;

	i = 0;
	while (s1[i])
	{
		j = 0;
		while (s2[j])
		{
			if (s1[i] == s2[j])
				return ((char *)&s1[i]);
			j++;
		}
		i++;
	}
	return (NULL);
}

int	main(int argc, char **argv)
{
	if (argc == 3)
	{
		printf ("%s", ft_strpbrk (argv[2], argv[3]));
	}
	printf("\n");
	return (0);
}
